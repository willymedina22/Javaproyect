    private void btnGuardarCambiosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarCambiosActionPerformed
        //todos los datos de la mascota
        String nombreMasco = txtNombre.getText();
        String raza = txtRaza.getText();
        String color = txtColor.getText();
        String observaciones = txtObservaciones.getText();
        String alergico = (String) cmbAlergico.getSelectedItem();
        String atencionEspecial = (String) cmbAtencionEspecial.getSelectedItem();
        //todos los datos del dueño
        String nombreDuenio = txtDuenio.getText();
        String celDuenio = txtCelDuenio.getText();
        
        control.modificarMascota(mascota, nombreMasco, raza, color, observaciones, alergico, atencionEspecial, nombreDuenio, celDuenio);
        
        mostrarMensaje("Edicion realizda correctamente", "info", "Edicion exitosa");
    }//GEN-LAST:event_btnGuardarCambiosActionPerformed

